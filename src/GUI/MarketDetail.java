    public void tampilLoanDetail(String cari){
        try {
            sql = "SELECT * FROM loan WHERE id_loan = "+cari;
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                tfCompanyLD.setText(rs.getString("company"));
                tfRateLD.setText(rs.getString("rate"));
                tfGradeLD.setText(rs.getString("grade"));
                tfTenureLD.setText(rs.getString("tenure"));
                jxDateLD.setDate(rs.getDate("date_added"));
            }
            
        } catch (Exception e) {
            System.out.println("ERROR"+e.getMessage());
        }
    }
    
    public void cariBalance(){
        try {
            sql = "SELECT * FROM user WHERE id_user = "+idnya;
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                balance = rs.getInt("balance");
            }
        } catch (Exception e) {
            System.out.println("ERROR"+e.getMessage());
        }
    }
    
    public void updateBalance(){
            int beli = Integer.parseInt(tfAmountLD.getText());

            try {
                sql = "UPDATE user SET "
                    + "balance = ? "
                    + "WHERE id_user="+idnya;
                    ps = conn.prepareStatement(sql);
                    ps.setInt(1, balance-beli);

                    int rt = ps.executeUpdate();
            } catch (Exception e) {
                System.out.println("ERROR"+e.getMessage());
            }
    }
