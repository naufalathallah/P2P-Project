    private void btBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuyActionPerformed
        
        int n = JOptionPane.showConfirmDialog(null, "Are you sure want to buy?",
                                                    "Buy",
                                                    JOptionPane.YES_NO_CANCEL_OPTION,
                                                    JOptionPane.QUESTION_MESSAGE, null);
        
        if(n == 0) {
            if(balance >= Integer.parseInt(tfAmountLD.getText())){
                
                SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");
                String company = tfCompanyLD.getText();
                int rate = Integer.parseInt(tfRateLD.getText());
                String grade = tfGradeLD.getText();
                int tenure = Integer.parseInt(tfTenureLD.getText());
                String tgl = formater.format(jxDateLD.getDate());
                int amount = Integer.parseInt(tfAmountLD.getText());
                int total = Integer.parseInt(tfTotalLD.getText());

                try {
                    sql = "INSERT INTO purchase VALUES ( ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
                    ps = conn.prepareStatement(sql);
                    ps.setInt(1, 0);
                    ps.setString(2, company);
                    ps.setInt(3, rate);
                    ps.setString(4, grade);
                    ps.setInt(5, tenure);
                    ps.setString(6, tgl);
                    ps.setInt(7, amount);
                    ps.setInt(8, total);
                    ps.setInt(9, idnya);
                    ps.setInt(10, idloan);

                    int rt = ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Succeed","Information",JOptionPane.INFORMATION_MESSAGE);
                    updateBalance();
                } catch (Exception e) {
                    System.out.println("ERROR"+e.getMessage());
                }
            }else {
                JOptionPane.showMessageDialog(null, "Insufficient Balance","Information",JOptionPane.INFORMATION_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_btBuyActionPerformed

