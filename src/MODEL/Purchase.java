package MODEL;

/**
 *
 * @author NAUFAL
 */
public class Purchase {
    private int id,rate,amount,total;
    private String company;
    
    public Purchase(int id,String company, int rate) {
        this.id=id;
        this.company=company;
        this.rate=rate;
    }

    public Purchase(int id,String company, int rate, int amount, int total) {
        this.id=id;
        this.company=company;
        this.rate=rate;
        this.amount=amount;
        this.total=total;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the rate
     */
    public int getRate() {
        return rate;
    }

    /**
     * @param rate the rate to set
     */
    public void setRate(int rate) {
        this.rate = rate;
    }

    /**
     * @return the company
     */
    public String getCompany() {
        return company;
    }

    /**
     * @param company the company to set
     */
    public void setCompany(String company) {
        this.company = company;
    }
}
